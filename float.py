"""
This module demonstrates the different types of division in Python.
It performs standard division, addition with a float, and floor division,
and then prints the type of the result of each operation.
"""

X = 12/4
Y = 5 + 6.1
Z = 12//4

print(f"Type of x is {type(X)}")
print(f"Type of y is {type(Y)}")
print(f"Type of z is {type(Z)}")

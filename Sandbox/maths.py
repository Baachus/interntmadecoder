"""
Maths module
"""
import math

# Calculate square root
sqrt_value = math.sqrt(64)
print(f'Square root of 64: {sqrt_value}')

# Calculate factorial
factorial_value = math.factorial(5)
print(f'Factorial of 5: {factorial_value}')

# Trigonometric functions
sin_value = math.sin(math.radians(30))
print(f'Sin of 30 degrees: {sin_value}')

# Constants
print(f'Value of Pi: {math.pi}')
print(f'Value of E: {math.e}')
